/**
 * Pre-computed map for faster word lookups by chapter.
 * Key: Chapter ID (number)
 * Value: Array of WordInfo objects for that chapter.
 * The words within each chapter's array are sorted by length descending
 * to ensure longer words are matched first in the ChapterTextRenderer.
 */
export const wordsByChapter: Record<number, WordInfo[]> = rawWordData.reduce(
  (acc, chapterWordInfo) => {
    const { textId, ...wordData } = chapterWordInfo;
    if (!acc[textId]) {
      acc[textId] = [];
    }
    // Only add if the word isn't already in the list for this chapter to avoid duplicates from raw data
    // This check ensures that each word is unique per chapter in wordsByChapter.
    if (!acc[textId].some((existing) => existing.word === wordData.word)) {
      acc[textId].push(wordData);
    }
    return acc;
  },
  {} as Record<number, WordInfo[]>
);

// Sort words within each chapter by length descending AFTER the initial grouping
for (const chapterId in wordsByChapter) {
  wordsByChapter[chapterId].sort((a, b) => {
    if (a.word && b.word) {
      return b.word.length - a.word.length;
    }
    if (a.word) return -1; // a comes first if b.word is undefined
    if (b.word) return 1;  // b comes first if a.word is undefined
    return 0; // both undefined, order doesn't matter
  });
}


/**
 * Gets the pre-sorted word list for a specific chapter, filtered by presence in chapterContent.
 * Used by ChapterTextRenderer for highlighting.
 *
 * @param chapterId The ID of the chapter.
 * @param chapterContent The actual text content of the chapter.
 * @returns An array of WordInfo objects sorted by length descending, or an empty array if not found or content is empty.
 */
export const getWordsForChapter = (chapterId: number, chapterContent: string): WordInfo[] => {
  const chapterWords = wordsByChapter[chapterId] || [];

  // If chapterContent is empty or not provided, or if no words are defined for the chapter, return an empty array.
  if (!chapterContent || chapterWords.length === 0) {
    // console.log(`getWordsForChapter: Chapter content for chapter ${chapterId} is empty or no words defined. Returning empty array.`);
    return [];
  }
  
  // Filter words to include only those present in the chapterContent.
  // This ensures that only words actually found in the text are made interactive.
  const filteredWords = chapterWords.filter(wordInfo => {
    if (wordInfo && typeof wordInfo.word === 'string') { // Ensure wordInfo and wordInfo.word are valid
      return chapterContent.includes(wordInfo.word);
    }
    return false; // Exclude if wordInfo or wordInfo.word is invalid
  });
  // console.log(`getWordsForChapter: Retrieving words for chapter ${chapterId}. Raw count: ${chapterWords.length}, Filtered by content: ${filteredWords.length}`);
  return filteredWords;
};


/**
 * Pre-computed map for direct word lookups (pinyin, translation).
 * Key: Chinese word (string)
 * Value: WordInfo object (pinyin, translation).
 * This map stores unique words across all chapters for fast direct lookup.
 * If a word appears in multiple chapters with different info, the last one in rawWordData wins for this global map.
 */
const allWordsMap: Map<string, WordInfo> = new Map();
rawWordData.forEach(({ textId, ...wordInfo }) => {
  // Ensure wordInfo.word is a string before setting it in the map
  if (wordInfo && typeof wordInfo.word === 'string' && wordInfo.word.length > 0) {
    allWordsMap.set(wordInfo.word, { word: wordInfo.word, pinyin: wordInfo.pinyin, translation: wordInfo.translation });
  }
});

/**
 * Gets the word information (pinyin, translation) directly from the pre-computed map.
 * This is much faster than iterating through chapter-specific lists for individual word lookups.
 *
 * @param word The Chinese word to look up.
 * @returns The WordInfo object if found, otherwise null.
 */
export function getWordInfo(word: string): WordInfo | null {
  const start = performance.now();
  const result = allWordsMap.get(word) || null;
  const end = performance.now();
  // console.log(`getWordInfo (direct map lookup) for "${word}" took ${end - start}ms. Found: ${!!result}`);
  return result;
}
